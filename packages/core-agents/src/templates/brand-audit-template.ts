export interface BrandAuditData {
  overallScore: number;
  campaigns: Array<{
    id: string;
    name: string;
    brandScore: number;
    issues: string[];
    strengths: string[];
  }>;
  issueBreakdown: {
    tone: number;
    visual: number;
    messaging: number;
    consistency: number;
  };
  recommendations: string[];
  trendAnalysis: Array<{
    month: string;
    score: number;
    improvement: number;
  }>;
}

