  private calculateAgentGrade(agent: any): string {
    const successRate = agent.goalSuccessRate || 0;
    if (successRate >= 0.9) return 'A+';
    if (successRate >= 0.8) return 'A';
    if (successRate >= 0.7) return 'B';
    return 'C';
  }

